        Socket socket = new Socket("localhost", 9090);
        System.out.println("Client is Running.....");

        dis = new DataInputStream(socket.getInputStream());
        dos = new DataOutputStream(socket.getOutputStream());

        // Read analysis' results from server
        longestWord = dis.readUTF();
        shortestWord = dis.readUTF();
        numOfWords = dis.readInt();
        numOfLetters = dis.readInt();
        
        repenTable = dis.readUTF();
        numOfRepet = repenTable.split(", ");
