    // Open file browser
    private void openButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openButtonActionPerformed
        filePathTextField.setEditable(false);
        // JFileChooser points to user's default directory 
        j = new JFileChooser();

        // Open the save dialog 
        r = j.showSaveDialog(null);

        // if the user selects a file 
        if (r == JFileChooser.APPROVE_OPTION) {

            // set the label to the path of the selected file
            filePathTextField.setText(j.getSelectedFile().getAbsolutePath());
        } else {
            filePathTextField.setText("You cancelled the operation!");
        }
    }//GEN-LAST:event_openButtonActionPerformed

    // Do analysis on file
    private void doAnalysisButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doAnalysisButtonActionPerformed
        File file = j.getSelectedFile();
        try {
            BufferedReader br = new BufferedReader(new FileReader(file));

            String content;
            while ((content = br.readLine()) != null) {
                dos.writeUTF(content);
            }
        } catch (FileNotFoundException ex) {
            System.err.println(ex);
        } catch (IOException ex) {
            System.out.println(ex);
        }

        longestWordTextField.setText(longestWord);
        shortestWordTextField.setText(shortestWord);
        numOfLettersTextField.setText("" + numOfLetters);
        numOfWordsTextField.setText("" + numOfWords);

        try {
            numOfRepetTextField.setText(null);
            for (String str : numOfRepet) {
                numOfRepetTextField.append(str + "\n\n");
            }
        } catch (Exception e) {
            System.out.println("HashTable Converting Warning!");
        }
    }//GEN-LAST:event_doAnalysisButtonActionPerformed

